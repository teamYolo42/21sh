#include "includes/libft.h"

void		ft_putuint(unsigned int nb)
{
	if (nb >= 10)
	{
		ft_putuint(nb / 10);
		ft_putuint(nb % 10);
	}
	else
	{
		ft_putchar(nb + '0');
	}
}
