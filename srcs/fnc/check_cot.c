#include "../../includes/21sh.h"

int		check_cot(const char *str)
{
    int i;
    int ck;

    i = 0;
    ck = 0;
    while (str[i])
    {
        if (str[i] == '"' || str[i] == 39)
            ck++;
        i++;
    }
    return (((ck % 2) == 0) ? 0 : 1);
}